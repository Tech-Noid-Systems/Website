<h1> Overview </h1>
<p>We will outline how to stream on the station using the edcast standalone application and the LAME mp3 library. This is the easiset solution for streaming using windows. Feel free to use whatever solition works best for you, however this is the option that we will support at the moment. As more walk-throughs are available, we will expand this information.</p>
<p>To stream on the station you will need to perform the following steps:</p>
</center>
